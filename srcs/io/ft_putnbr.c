#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
		ft_putchar('-');
	if (n > 9 || n < -9)
		ft_putnbr(ABS((n / 10)));
	ft_putchar(ABS((n % 10)) + '0');
}
