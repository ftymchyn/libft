#include "libft.h"

char	*ft_strmap(const char *s, char (*f)(char))
{
	size_t	i;
	char	*str;

	if (!s || !f)
		return (0);
	i = 0;
	str = ft_strnew(ft_strlen(s));
	if (!str)
		return (0);
	while (s[i])
	{
		str[i] = (*f)(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
