int	ft_sqrt(int nb)
{
	int res;

	if (nb == 1)
		return (1);
	else if (nb < 4)
		return (0);
	if (100000000 <= nb)
		res = 10000;
	else
		res = 2;
	while ((res * res) < nb)
		res++;
	return ((res * res) == nb ? res : 0);
}
